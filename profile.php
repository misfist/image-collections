                    <p>Growing up, I had many interests. There was the underlying creative, handy side and the very sporty, athletic and social side that I embraced. As a teenager, I fantasized about being a fashion Designer, a Photographer or an Olympic athlete. All of which didn’t exist as career options in a small Caribbean country like Guyana.</p>

