						<p>Beach House, Costa Rica&nbsp;&nbsp;&nbsp;&nbsp;
						This is a very different and exciting, tropical project designed by EBH and Seema Singh. More than two years were spent visiting and revisiting Costa Rica to learn more about its very wet climate, lush and natural environment, way of life of the residences and how different materials age in such a climate.</p> 

						<p>Every year, after every trip, different designs were drawn up and floor plans were revised to accommodate a practical daily living and a very wet climate. Finally, a design was decided upon and a beach house was created with a more natural and organic sensibility using thatched palms  for the roof and wood for the main structure.</p>

						<p>Because of the amount of rainfall in Costa Rica, their dream of a flat Bauhaus-like roof was certainly not an option. Instead, the focus was all ABOUT the roof.  The rain falls in every direction, vertically, diagonally and sometimes even horizontally, helped by the wind. So even with the best overhang, being hit with a little bit of drizzle is a way of life in Costa Rica…</p>

						<p>A long time ago, every house in this old fishing village was built with a thatched roof (indigenous and non-indigenous) as told by the elders. Now, most of them have been replaced with galvanized sheets or </p>

						<p>Since Ebh and Seema decided to build the traditional way in terms of material, they still wanted a very sleek, contemporary look
						with very clean lines, a Zen like feel and openness to the elements.</p>

						<p>When the weather is hot, the freshness and coolness of a thatched roof in comparison to a galvanized or tiled roof can be tremendous. The pitch of the roof needs to be 45 degrees so that the water drains easily from the palms, resulting in a very high cathedral-like ceiling; thus, creating air-flow and ventilation.</p>

						<p>Selection of the palm leaves, time of the month the palms are cut, and drying of the palms, all play a very important role in building the best thatched roof possible. The local indigenous people say  the leaves have to be cut a few days before or after  “la luna” (the moon), otherwise the palms would be eaten by bugs and insects. In Costa Rica, indigenous people still build thatched palm roofs and they are the only ones permitted to cut the palm leaves, since the palms are now protected.</p>

						<p>At the end, what was created was a wooden, tropical beach house with a soaring cathedral-like ceiling of thatched palms. The rasters of the roof are supported by very tall, elegant, round columns, and the 52 feet long front deck flows beautifully and turns into an L shape.v

						<p>All of the columns were hand milled from square pieces of 
						raw lumber, and are a dominant element element 
						architecturally.</p>

						<p>On the upper level, a huge barn door and rows of bi-fold doors open the entire side and front, onto the deck, giving the sense of indoor-outdoor living. The windows in the kitchen swing up with ropes, giving a surf-like, beach feel with unobstructed tree canopy and ocean views.</p> 

						<p>Wooden slats and slatted windows on all sides of the house provide a semi-open feel and create cross ventilation and natural air flow.
						The lower level consists of a guest bedroom with its own private entrance, kitchen and bathroom and an expansive deck that can either function as a workspace or an entertainment area.</p>

						<p>Solar panels provide hot water and a rainwater recycling system is on its way…
